int		ft_sqrt(int nb)
{
	int		rc;

	rc = 1;
	if (nb < 1)
		return (0);
	while (rc < (nb / 2))
	{
		if (rc * rc == nb)
			return (rc);
		rc++;
	}
	return (0);
}
